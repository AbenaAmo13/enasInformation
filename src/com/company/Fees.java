package com.company;

public class Fees {
    private int totalFees,  amountFeesPaid, amountFeesPayable;

    public int getTotalFees() {
        return totalFees;
    }

    public void setTotalFees(int totalFees) {
        this.totalFees = totalFees;
    }

    public int getAmountFeesPaid() {
        return amountFeesPaid;
    }

    public void setAmountFeesPaid(int amountFeesPaid) {
        this.amountFeesPaid = amountFeesPaid;
    }

    public int getAmountFeesPayable() {
        return amountFeesPayable;
    }

    public void setAmountFeesPayable(int amountFeesPayable) {
        this.amountFeesPayable = amountFeesPayable;
    }
}
